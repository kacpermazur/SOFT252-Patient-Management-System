package singleton;

/**
 *
 * @author kacper
 */
public class UserManger {
    
    private volatile static UserManger _instance;
    
    public static UserManger getInstance()
    {
        if(_instance == null)
        {
            synchronized (UserManger.class)
            {
                if(_instance == null)
                {
                    _instance = new UserManger();
                }
            }
        }
    }
}
